export const generateEmojiGrid = (rows: number, cols: number, pairs: number): string[][] => {
  const grid: string[][] = Array(rows).fill(null).map(() => Array(cols).fill(''))
  const emojiPairs: string[] = []

  // Generate emoji pairs
  for (let i = 0; i < pairs; i++) {
    const emoji = emojis[Math.floor(Math.random() * emojis.length)]
    emojiPairs.push(emoji, emoji)
  }

  // Shuffle emoji pairs
  for (let i = emojiPairs.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [emojiPairs[i], emojiPairs[j]] = [emojiPairs[j], emojiPairs[i]]
  }

  // Place emojis in the grid
  let pairIndex = 0
  for (let row = 0; row < rows; row++) {
    for (let col = 0; col < cols; col++) {
      grid[row][col] = emojiPairs[pairIndex]
      pairIndex++
    }
  }

  return grid
}